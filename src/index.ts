function createAuthenticationChallengeCapturingFetch(input: {
  oauthProvider: PendingOAuthProvider | undefined;
  onAuthenticateHeader: (header: string | null) => void;
  serverUrl: URL;
  transportProxy: UseMcpTransportProxy | undefined;
}): FetchLike {
  return async (url, init) => {
    const requestedUrl = new URL(url.toString());
    const transportProxyUrl = normalizeTransportProxyUrl(input.transportProxy);
    const shouldProxyTransport =
      transportProxyUrl &&
      isSameUrlWithoutHash(requestedUrl, input.serverUrl) &&
      isMcpTransportMethod(init?.method);
    if (shouldProxyTransport && isMcpTransportSseProbe(init?.method)) {
      return new Response("SSE is not enabled for this stateless transport proxy", {
        status: 405,
        statusText: "Method Not Allowed",
      });
    }
    const response = await fetch(
      shouldProxyTransport ? transportProxyUrl : requestedUrl,
      shouldProxyTransport ? createTransportProxyInit(init, input.serverUrl) : init,
    );
    const requestedUrlString = requestedUrl.toString();

    if (response.ok && isProtectedResourceMetadataUrl(requestedUrlString)) {
      input.oauthProvider?.captureDiscoveredResourceMetadataUrl(requestedUrlString);
    }

    if (response.status === 401) {
      const authenticateHeader = response.headers.get("www-authenticate");
      input.onAuthenticateHeader(authenticateHeader);

      if (parseBearerAuthRequirement(authenticateHeader)) {
        await response.body?.cancel();
        throw new Error("Bearer token required.");
      }
      const sanitizedResponse = removeNonBearerResourceMetadataChallenge(
        response,
        authenticateHeader,
      );
      if (sanitizedResponse) {
        return sanitizedResponse;
      }
    }

    return response;
  };
}

function removeNonBearerResourceMetadataChallenge(
  response: Response,
  authenticateHeader: string | null,
): Response | null {
  if (
    !authenticateHeader ||
    findWwwAuthenticateChallenge(authenticateHeader, "Bearer")?.parameters.get(
      "resource_metadata",
    ) ||
    !readWwwAuthenticateParameter(authenticateHeader, "resource_metadata")
  ) {
    return null;
  }

  const headers = new Headers(response.headers);
  headers.delete("www-authenticate");

  return new Response(response.body, {
    headers,
    status: response.status,
    statusText: response.statusText,
  });
}

function isSameUrlWithoutHash(left: URL, right: URL): boolean {
  const normalizedLeft = new URL(left.toString());
  const normalizedRight = new URL(right.toString());
  normalizedLeft.hash = "";
  normalizedRight.hash = "";

  return normalizedLeft.toString() === normalizedRight.toString();
}

function isMcpTransportMethod(method: string | undefined): boolean {
  const normalizedMethod = (method ?? "GET").toUpperCase();

  return normalizedMethod === "DELETE" || normalizedMethod === "GET" || normalizedMethod === "POST";
}

function isMcpTransportSseProbe(method: string | undefined): boolean {
  return (method ?? "GET").toUpperCase() === "GET";
}

function createTransportProxyInit(init: RequestInit | undefined, serverUrl: URL): RequestInit {
  const headers = new Headers(init?.headers);
  headers.set("x-mcp-target-url", mcpTransportTargetUrl(serverUrl).toString());

  return {
    ...init,
    credentials: "omit",
    headers,
  };
}

function mcpTransportTargetUrl(serverUrl: URL): URL {
  const targetUrl = new URL(serverUrl.toString());
  targetUrl.hash = "";

  return targetUrl;
}

function isProtectedResourceMetadataUrl(url: string): boolean {
  try {
    return new URL(url).pathname.includes("/.well-known/oauth-protected-resource");
  } catch {
    return false;
  }
}

function parseBearerAuthRequirement(header: string | null): McpAuthRequirement | null {
  const bearerChallenge = findWwwAuthenticateChallenge(header, "Bearer");
  if (!bearerChallenge) {
    return null;
  }

  if (bearerChallenge.parameters.get("resource_metadata")) {
    return null;
  }
  if (
    bearerChallenge.parameters.has("error") ||
    bearerChallenge.parameters.has("error_description") ||
    bearerChallenge.parameters.has("error_uri")
  ) {
    return null;
  }

  const scope = bearerChallenge.parameters.get("scope");
  const scopes = scope ? scope.split(/\s+/u).filter(Boolean) : undefined;
  const realm = bearerChallenge.parameters.get("realm");

  return {
    ...(realm ? { realm } : {}),
    reason: "oauth_metadata_absent",
    ...(scopes ? { scopes } : {}),
    type: "bearer",
  };
}

function readWwwAuthenticateParameter(
  header: string,
  parameterName: string,
  scheme?: string,
): string | null {
  for (const challenge of parseWwwAuthenticateChallenges(header)) {
    if (scheme && challenge.scheme.toLowerCase() !== scheme.toLowerCase()) {
      continue;
    }
    const parameter = challenge.parameters.get(parameterName.toLowerCase());
    if (parameter != null) {
      return parameter;
    }
  }

  return null;
}

function findWwwAuthenticateChallenge(
  header: string | null,
  scheme: string,
): { parameters: Map<string, string>; scheme: string } | null {
  if (!header) {
    return null;
  }

  return (
    parseWwwAuthenticateChallenges(header).find(
      (challenge) => challenge.scheme.toLowerCase() === scheme.toLowerCase(),
    ) ?? null
  );
}

function parseWwwAuthenticateChallenges(
  header: string,
): Array<{ parameters: Map<string, string>; scheme: string }> {
  const challenges: Array<{ parameters: Map<string, string>; scheme: string }> = [];
  let index = 0;

  while (index < header.length) {
    index = skipHeaderWhitespaceAndCommas(header, index);
    const scheme = readHeaderToken(header, index);
    if (!scheme.value) {
      break;
    }
    index = scheme.nextIndex;
    const parameters = new Map<string, string>();

    while (index < header.length) {
      index = skipHeaderWhitespace(header, index);
      if (header[index] === ",") {
        const afterComma = skipHeaderWhitespace(header, index + 1);
        const nextToken = readHeaderToken(header, afterComma);
        const afterNextToken = skipHeaderWhitespace(header, nextToken.nextIndex);
        if (nextToken.value && header[afterNextToken] !== "=") {
          index = afterComma;
          break;
        }
        index = afterComma;
      }

      const key = readHeaderToken(header, index);
      index = skipHeaderWhitespace(header, key.nextIndex);
      if (!key.value || header[index] !== "=") {
        break;
      }
      index = skipHeaderWhitespace(header, index + 1);

      const value = readHeaderParameterValue(header, index);
      parameters.set(key.value.toLowerCase(), value.value);
      index = value.nextIndex;
    }

    challenges.push({
      parameters,
      scheme: scheme.value,
    });
  }

  return challenges;
}

function skipHeaderWhitespaceAndCommas(header: string, index: number): number {
  let nextIndex = index;
  while (nextIndex < header.length && /[\s,]/u.test(header[nextIndex]!)) {
    nextIndex += 1;
  }

  return nextIndex;
}

function skipHeaderWhitespace(header: string, index: number): number {
  let nextIndex = index;
  while (nextIndex < header.length && /\s/u.test(header[nextIndex]!)) {
    nextIndex += 1;
  }

  return nextIndex;
}

function readHeaderToken(header: string, index: number): { nextIndex: number; value: string } {
  let nextIndex = index;
  while (nextIndex < header.length && /[!#$%&'*+\-.^_`|~0-9A-Za-z]/u.test(header[nextIndex]!)) {
    nextIndex += 1;
  }

  return {
    nextIndex,
    value: header.slice(index, nextIndex),
  };
}

function readHeaderParameterValue(
  header: string,
  index: number,
): { nextIndex: number; value: string } {
  if (header[index] !== '"') {
    return readHeaderToken(header, index);
  }

  let nextIndex = index + 1;
  let value = "";
  while (nextIndex < header.length) {
    const char = header[nextIndex]!;
    if (char === "\\") {
      const escaped = header[nextIndex + 1];
      if (escaped) {
        value += escaped;
        nextIndex += 2;
        continue;
      }
    }
    if (char === '"') {
      return {
        nextIndex: nextIndex + 1,
        value,
      };
    }
    value += char;
    nextIndex += 1;
  }

  return {
    nextIndex,
    value,
  };
}

type ManualOAuthClientInference = {
  authorizationServerMetadataUrl: string;
  requirement: McpManualOAuthClientRequirement;
  resourceMetadataUrl: string;
};

async function inferManualOAuthClientRequirement(
  serverUrl: URL,
  authenticateHeader: string | null,
): Promise<ManualOAuthClientInference | null> {
  const resourceMetadataUrl = authenticateHeader
    ? readWwwAuthenticateParameter(authenticateHeader, "resource_metadata", "Bearer")
    : null;
  if (!resourceMetadataUrl) {
    return null;
  }

  try {
    const resourceMetadataResponse = await fetch(resourceMetadataUrl);
    if (!resourceMetadataResponse.ok) {
      return null;
    }
    const resourceMetadata = parseProtectedResourceMetadataSubset(
      await resourceMetadataResponse.json(),
    );
    if (!resourceMetadata) {
      return null;
    }
    const resourceUrl = normalizeMcpUrl(resourceMetadata.resource);
    if (!resourceUrl || !isSameUrlWithoutHash(resourceUrl, serverUrl)) {
      return null;
    }
    const authorizationServer = resourceMetadata.authorization_servers?.[0];
    if (!authorizationServer) {
      return null;
    }

    const metadataUrl = authorizationServerMetadataUrlFor(authorizationServer);
    const metadataResponse = await fetch(metadataUrl);
    if (!metadataResponse.ok) {
      return null;
    }
    const metadata = parseAuthorizationServerMetadataSubset(await metadataResponse.json());
    if (!metadata) {
      return null;
    }

    if (metadata.registration_endpoint || metadata.client_id_metadata_document_supported) {
      return null;
    }

    return {
      authorizationServerMetadataUrl: metadataUrl,
      requirement: {
        ...(metadata.authorization_endpoint
          ? { authorizationEndpoint: metadata.authorization_endpoint }
          : {}),
        issuer: metadata.issuer ?? new URL(authorizationServer).toString(),
        reason: "client_registration_unavailable",
        suggestedFields: ["clientId"],
        supportsClientMetadataDocument: false,
        supportsDynamicClientRegistration: false,
        ...(metadata.token_endpoint ? { tokenEndpoint: metadata.token_endpoint } : {}),
        type: "manual_oauth_client",
      },
      resourceMetadataUrl,
    };
  } catch {
    return null;
  }
}

function parseProtectedResourceMetadataSubset(value: unknown): {
  authorization_servers?: string[];
  resource: string;
} | null {
  if (!isRecord(value)) {
    return null;
  }

  const resource = value.resource;
  const authorizationServers = value.authorization_servers;
  if (
    typeof resource !== "string" ||
    (authorizationServers !== undefined && !isStringArray(authorizationServers))
  ) {
    return null;
  }

  return {
    ...(authorizationServers ? { authorization_servers: authorizationServers } : {}),
    resource,
  };
}

function parseAuthorizationServerMetadataSubset(value: unknown): {
  authorization_endpoint?: string;
  client_id_metadata_document_supported?: boolean;
  issuer?: string;
  registration_endpoint?: string;
  token_endpoint?: string;
} | null {
  if (!isRecord(value)) {
    return null;
  }

  const issuer = value.issuer;
  const authorizationEndpoint = value.authorization_endpoint;
  const tokenEndpoint = value.token_endpoint;
  const registrationEndpoint = value.registration_endpoint;
  const supportsClientMetadataDocument = value.client_id_metadata_document_supported;
  if (
    (issuer !== undefined && typeof issuer !== "string") ||
    (authorizationEndpoint !== undefined && typeof authorizationEndpoint !== "string") ||
    (tokenEndpoint !== undefined && typeof tokenEndpoint !== "string") ||
    (registrationEndpoint !== undefined && typeof registrationEndpoint !== "string") ||
    (supportsClientMetadataDocument !== undefined &&
      typeof supportsClientMetadataDocument !== "boolean")
  ) {
    return null;
  }

  return {
    ...(authorizationEndpoint ? { authorization_endpoint: authorizationEndpoint } : {}),
    ...(supportsClientMetadataDocument !== undefined
      ? { client_id_metadata_document_supported: supportsClientMetadataDocument }
      : {}),
    ...(issuer ? { issuer } : {}),
    ...(registrationEndpoint ? { registration_endpoint: registrationEndpoint } : {}),
    ...(tokenEndpoint ? { token_endpoint: tokenEndpoint } : {}),
  };
}

function isRecord(value: unknown): value is Record<string, unknown> {
  return value !== null && typeof value === "object" && !Array.isArray(value);
}

function isStringArray(value: unknown): value is string[] {
  return Array.isArray(value) && value.every((item) => typeof item === "string");
}

const customStorageIds = new WeakMap<McpStorage, number>();
let nextCustomStorageId = 1;

function getMcpStorageDependencyKey(storage: McpStorage | false | undefined): string {
  if (storage === false) {
    return "memory";
  }
  if (!storage) {
    return "default";
  }

  const existingId = customStorageIds.get(storage);
  if (existingId) {
    return `custom:${existingId}`;
  }

  const id = nextCustomStorageId;
  nextCustomStorageId += 1;
  customStorageIds.set(storage, id);

  return `custom:${id}`;
}

