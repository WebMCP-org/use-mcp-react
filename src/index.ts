function sha256Hex(value: string): string {
  const bytes = new TextEncoder().encode(value);
  const bitLength = bytes.length * 8;
  const paddedLength = Math.ceil((bytes.length + 9) / 64) * 64;
  const padded = new Uint8Array(paddedLength);
  padded.set(bytes);
  padded[bytes.length] = 0x80;

  const view = new DataView(padded.buffer);
  view.setUint32(paddedLength - 8, Math.floor(bitLength / 0x100000000));
  view.setUint32(paddedLength - 4, bitLength >>> 0);

  let h0 = 0x6a09e667;
  let h1 = 0xbb67ae85;
  let h2 = 0x3c6ef372;
  let h3 = 0xa54ff53a;
  let h4 = 0x510e527f;
  let h5 = 0x9b05688c;
  let h6 = 0x1f83d9ab;
  let h7 = 0x5be0cd19;
  const words = new Uint32Array(64);

  for (let offset = 0; offset < paddedLength; offset += 64) {
    for (let index = 0; index < 16; index += 1) {
      words[index] = view.getUint32(offset + index * 4);
    }

    for (let index = 16; index < 64; index += 1) {
      const s0 =
        rotateRight(words[index - 15]!, 7) ^
        rotateRight(words[index - 15]!, 18) ^
        (words[index - 15]! >>> 3);
      const s1 =
        rotateRight(words[index - 2]!, 17) ^
        rotateRight(words[index - 2]!, 19) ^
        (words[index - 2]! >>> 10);
      words[index] = (words[index - 16]! + s0 + words[index - 7]! + s1) >>> 0;
    }

    let a = h0;
    let b = h1;
    let c = h2;
    let d = h3;
    let e = h4;
    let f = h5;
    let g = h6;
    let h = h7;

    for (let index = 0; index < 64; index += 1) {
      const s1 = rotateRight(e, 6) ^ rotateRight(e, 11) ^ rotateRight(e, 25);
      const choice = (e & f) ^ (~e & g);
      const temp1 = (h + s1 + choice + SHA256_K[index]! + words[index]!) >>> 0;
      const s0 = rotateRight(a, 2) ^ rotateRight(a, 13) ^ rotateRight(a, 22);
      const majority = (a & b) ^ (a & c) ^ (b & c);
      const temp2 = (s0 + majority) >>> 0;

      h = g;
      g = f;
      f = e;
      e = (d + temp1) >>> 0;
      d = c;
      c = b;
      b = a;
      a = (temp1 + temp2) >>> 0;
    }

    h0 = (h0 + a) >>> 0;
    h1 = (h1 + b) >>> 0;
    h2 = (h2 + c) >>> 0;
    h3 = (h3 + d) >>> 0;
    h4 = (h4 + e) >>> 0;
    h5 = (h5 + f) >>> 0;
    h6 = (h6 + g) >>> 0;
    h7 = (h7 + h) >>> 0;
  }

  return [h0, h1, h2, h3, h4, h5, h6, h7]
    .map((word) => word.toString(16).padStart(8, "0"))
    .join("");
}

function rotateRight(value: number, bits: number): number {
  return (value >>> bits) | (value << (32 - bits));
}

async function readStorageJson<T>(
  storage: McpStorage | null,
  key: string,
  parse: (value: unknown) => T | undefined,
): Promise<T | undefined> {
  if (!storage) {
    return undefined;
  }

  const storedValue = await storage.getItem(key);
  if (!storedValue) {
    return undefined;
  }

  try {
    return parse(JSON.parse(storedValue));
  } catch {
    await storage.removeItem(key);
    return undefined;
  }
}

async function writeStorageJson(
  storage: McpStorage | null,
  key: string,
  value: unknown,
): Promise<void> {
  if (!storage) {
    return;
  }

  await storage.setItem(key, JSON.stringify(value));
}

function parseStoredClientInformation(value: unknown): OAuthClientInformationMixed | undefined {
  if (!isRecord(value) || typeof value.client_id !== "string") {
    return undefined;
  }

  return value as OAuthClientInformationMixed;
}

function parseStoredTokens(value: unknown): OAuthTokens | undefined {
  if (
    !isRecord(value) ||
    typeof value.access_token !== "string" ||
    typeof value.token_type !== "string"
  ) {
    return undefined;
  }

  return value as OAuthTokens;
}

function parseStoredDiscoveryState(value: unknown): OAuthDiscoveryState | undefined {
  if (!isRecord(value) || typeof value.authorizationServerUrl !== "string") {
    return undefined;
  }

  return value as unknown as OAuthDiscoveryState;
}

function parseStoredString(value: unknown): string | undefined {
  return typeof value === "string" ? value : undefined;
}

function isMcpOAuthCallbackMessage(value: unknown): value is McpOAuthCallbackMessage {
  return (
    isRecord(value) &&
    value.type === "use-mcp-react:oauth-callback" &&
    (value.code === undefined || typeof value.code === "string") &&
    (value.error === undefined || typeof value.error === "string") &&
    (value.errorDescription === undefined || typeof value.errorDescription === "string") &&
    (value.state === undefined || typeof value.state === "string")
  );
}

function createStaticBearerAuthProvider(bearerToken: string): OAuthClientProvider {
  return new StaticBearerAuthProvider(bearerToken);
}

class PendingOAuthProvider implements OAuthClientProvider {
  readonly clientMetadata: OAuthClientMetadata;
  readonly clientMetadataUrl?: string;
  readonly redirectUrl: string;

  private authorizationUrl: URL | null = null;
  private client: OAuthClientInformationMixed | undefined;
  private discoveredResourceMetadataUrl: string | undefined;
  private oauthState: string | undefined;
  private parsedResourceMetadataUrlFromChallenge: string | undefined;
  private savedTokens: OAuthTokens | undefined;
  private savedDiscoveryState: OAuthDiscoveryState | undefined;
  private readonly storage: McpStorage | null;
  private readonly storageKeys: OAuthStorageKeys;
  private target: AuthorizationTarget | null = null;
  private verifier: string | undefined;
  private usedClientMetadataDocument = false;
  private usedDynamicClientRegistration = false;

  constructor(
    serverUrl: URL,
    oauthOptions: UseMcpOAuthOptions = {},
    storage: McpStorage | null = null,
  ) {
    this.redirectUrl = resolveOAuthRedirectUrl(oauthOptions.redirectUrl);
    this.clientMetadataUrl = oauthOptions.clientMetadataUrl;
    this.storage = storage;
    this.clientMetadata = {
      client_name: "use-mcp-react browser client",
      grant_types: ["authorization_code", "refresh_token"],
      redirect_uris: [this.redirectUrl],
      response_types: ["code"],
      token_endpoint_auth_method: "none",
      ...oauthOptions.clientMetadata,
    };
    this.client = oauthOptions.clientId
      ? {
          client_id: oauthOptions.clientId,
        }
      : undefined;
    this.storageKeys = createOAuthStorageKeys(serverUrl, {
      clientId: oauthOptions.clientId,
      clientMetadata: this.clientMetadata,
      clientMetadataUrl: this.clientMetadataUrl,
      redirectUrl: this.redirectUrl,
    });
  }

  authRequirement(): Extract<McpAuthRequirement, { type: "oauth" }> | null {
    if (!this.authorizationUrl) {
      return null;
    }

    const scope = this.authorizationUrl.searchParams.get("scope");
    const scopes = scope ? scope.split(/\s+/u).filter(Boolean) : undefined;
    const issuer = this.authorizationServerIssuer();

    return {
      authorizationUrl: this.authorizationUrl,
      ...(issuer ? { issuer } : {}),
      ...(scopes ? { scopes } : {}),
      supportsClientMetadataDocument:
        this.savedDiscoveryState?.authorizationServerMetadata
          ?.client_id_metadata_document_supported === true,
      supportsDynamicClientRegistration: Boolean(
        this.savedDiscoveryState?.authorizationServerMetadata?.registration_endpoint,
      ),
      type: "oauth",
    };
  }

  authDiagnostics(): McpAuthDiagnostics | null {
    const requirement = this.authRequirement();
    if (!requirement) {
      return null;
    }

    return {
      issuer: requirement.issuer,
      authorizationServerMetadataUrl: this.savedDiscoveryState?.authorizationServerUrl
        ? authorizationServerMetadataUrlFor(this.savedDiscoveryState.authorizationServerUrl)
        : undefined,
      resourceMetadataUrl: this.resourceMetadataUrl(),
      registrationStrategy: this.usedClientMetadataDocument
        ? "client_metadata_url"
        : this.usedDynamicClientRegistration
          ? "dynamic_client_registration"
          : this.client
            ? "client_id"
            : undefined,
      scopes: requirement.scopes,
    };
  }

  captureAuthenticationChallenge(header: string | null): void {
    this.parsedResourceMetadataUrlFromChallenge = header
      ? (readWwwAuthenticateParameter(header, "resource_metadata", "Bearer") ?? undefined)
      : undefined;
  }

  captureDiscoveredResourceMetadataUrl(resourceMetadataUrl: string): void {
    this.discoveredResourceMetadataUrl = resourceMetadataUrl;
  }

  clearAuthenticationChallenge(): void {
    this.discoveredResourceMetadataUrl = undefined;
    this.parsedResourceMetadataUrlFromChallenge = undefined;
  }

  resourceMetadataUrl(): string | undefined {
    // Work around SDK 1.29.0 losing the PRM URL on GET 401 and internal discovery.
    // See docs/reference/sdk-resource-metadata-url-loss.md.
    return (
      this.savedDiscoveryState?.resourceMetadataUrl ??
      this.discoveredResourceMetadataUrl ??
      this.parsedResourceMetadataUrlFromChallenge
    );
  }

  async clientInformation(): Promise<OAuthClientInformationMixed | undefined> {
    if (this.client) {
      return this.client;
    }

    this.client = await readStorageJson(
      this.storage,
      this.storageKeys.client,
      parseStoredClientInformation,
    );
    if (this.client) {
      this.rememberRegistrationStrategy(this.client);
    }
    return this.client;
  }

  async saveClientInformation(clientInformation: OAuthClientInformationMixed): Promise<void> {
    this.client = clientInformation;
    this.rememberRegistrationStrategy(clientInformation);
    await writeStorageJson(this.storage, this.storageKeys.client, clientInformation);
  }

  async tokens(): Promise<OAuthTokens | undefined> {
    if (this.savedTokens) {
      return this.savedTokens;
    }

    this.savedTokens = await readStorageJson(
      this.storage,
      this.storageKeys.tokens,
      parseStoredTokens,
    );
    return this.savedTokens;
  }

  async state(): Promise<string> {
    this.oauthState = crypto.randomUUID();
    await writeStorageJson(this.storage, this.storageKeys.state, this.oauthState);

    return this.oauthState;
  }

  async saveTokens(tokens: OAuthTokens): Promise<void> {
    this.savedTokens = tokens;
    await writeStorageJson(this.storage, this.storageKeys.tokens, tokens);
  }

  async discoveryState(): Promise<OAuthDiscoveryState | undefined> {
    if (this.savedDiscoveryState) {
      return this.savedDiscoveryState;
    }

    this.savedDiscoveryState = await readStorageJson(
      this.storage,
      this.storageKeys.discovery,
      parseStoredDiscoveryState,
    );
    return this.savedDiscoveryState;
  }

  currentDiscoveryState(): OAuthDiscoveryState | undefined {
    return this.savedDiscoveryState;
  }

  async saveDiscoveryState(state: OAuthDiscoveryState): Promise<void> {
    this.savedDiscoveryState = state;
    await writeStorageJson(this.storage, this.storageKeys.discovery, state);
  }

  async invalidateCredentials(
    scope: "all" | "client" | "tokens" | "verifier" | "discovery",
  ): Promise<void> {
    if (scope === "all" || scope === "client") {
      this.client = undefined;
      this.usedClientMetadataDocument = false;
      this.usedDynamicClientRegistration = false;
      await this.storage?.removeItem(this.storageKeys.client);
    }
    if (scope === "all" || scope === "tokens") {
      this.savedTokens = undefined;
      await this.storage?.removeItem(this.storageKeys.tokens);
    }
    if (scope === "all" || scope === "verifier") {
      this.verifier = undefined;
      this.oauthState = undefined;
      await this.storage?.removeItem(this.storageKeys.verifier);
      await this.storage?.removeItem(this.storageKeys.state);
    }
    if (scope === "all" || scope === "discovery") {
      this.savedDiscoveryState = undefined;
      await this.storage?.removeItem(this.storageKeys.discovery);
    }
  }

  redirectToAuthorization(authorizationUrl: URL): void {
    this.authorizationUrl = new URL(authorizationUrl.toString());
    if (this.target && !this.target.closed) {
      this.target.location.href = this.authorizationUrl.toString();
      this.target.focus();
    }
    this.target = null;
  }

  async saveCodeVerifier(codeVerifier: string): Promise<void> {
    this.verifier = codeVerifier;
    await writeStorageJson(this.storage, this.storageKeys.verifier, codeVerifier);
  }

  async codeVerifier(): Promise<string> {
    const verifier =
      this.verifier ??
      (await readStorageJson(this.storage, this.storageKeys.verifier, parseStoredString));
    if (!verifier) {
      throw new Error("No OAuth PKCE verifier saved.");
    }

    this.verifier = verifier;
    return verifier;
  }

  pendingState(): string | null {
    return this.oauthState ?? this.authorizationUrl?.searchParams.get("state") ?? null;
  }

  setAuthorizationTarget(target: AuthorizationTarget): void {
    this.target = target;
  }

  private rememberRegistrationStrategy(clientInformation: OAuthClientInformationMixed): void {
    this.usedClientMetadataDocument =
      Boolean(this.clientMetadataUrl) && clientInformation.client_id === this.clientMetadataUrl;
    this.usedDynamicClientRegistration = !this.usedClientMetadataDocument;
  }

  private authorizationServerIssuer(): string | undefined {
    return (
      this.savedDiscoveryState?.authorizationServerMetadata?.issuer ??
      this.savedDiscoveryState?.authorizationServerUrl ??
      this.authorizationUrl?.origin
    );
  }
}

function prepareAuthorizationTarget(
  provider: PendingOAuthProvider | undefined,
  authorizationTarget: UseMcpActionOptions["authorizationTarget"],
  popupWindowName: () => string,
): PreparedAuthorizationTargetResult {
  if (!provider || !authorizationTarget) {
    return { ok: true };
  }

  if (authorizationTarget === "popup") {
    const popup = window.open(
      "about:blank",
      popupWindowName(),
      "width=600,height=700,resizable=yes,scrollbars=yes,status=yes",
    );
    if (!popup) {
      return { ok: false, reason: "popup_blocked" };
    }
    provider.setAuthorizationTarget(popup);
    popup.focus();
    return { ok: true, ownsTarget: true, target: popup };
  }

  provider.setAuthorizationTarget(authorizationTarget);
  authorizationTarget.focus();
  return { ok: true, ownsTarget: false, target: authorizationTarget };
}

function createOAuthPopupWindowName(): string {
  return `use-mcp-react-oauth-${crypto.randomUUID()}`;
}

function resolveOAuthRedirectUrl(redirectUrl: URL | string | undefined): string {
  const origin = typeof window === "undefined" ? "http://localhost" : window.location.origin;
  if (!redirectUrl) {
    return `${origin}/oauth/callback`;
  }

  return new URL(redirectUrl, origin).toString();
}

class StaticBearerAuthProvider implements OAuthClientProvider {
  readonly clientMetadata: OAuthClientMetadata = {
    client_name: "use-mcp-react static bearer client",
    grant_types: [],
    redirect_uris: [],
    response_types: [],
    token_endpoint_auth_method: "none",
  };

  readonly redirectUrl = undefined;

  constructor(private readonly bearerToken: string) {}

  clientInformation(): OAuthClientInformationMixed {
    return {
      client_id: "use-mcp-react-static-bearer",
      grant_types: [],
      redirect_uris: [],
      response_types: [],
      token_endpoint_auth_method: "none",
    };
  }

  async tokens(): Promise<OAuthTokens> {
    return {
      access_token: await this.readToken(),
      token_type: "Bearer",
    };
  }

  saveTokens(): void {
    throw new Error("Static bearer auth does not save OAuth tokens.");
  }

  redirectToAuthorization(): void {
    throw new Error("Static bearer auth does not redirect.");
  }

  saveCodeVerifier(): void {
    throw new Error("Static bearer auth does not use PKCE.");
  }

  codeVerifier(): string {
    throw new Error("Static bearer auth does not use PKCE.");
  }

  private async readToken(): Promise<string> {
    return this.bearerToken;
  }
}
