/**
 * Reads the current OAuth callback URL and delivers it to waiting hook
 * instances.
 *
 * `targetOrigin` is used only for `window.opener.postMessage`. The
 * `BroadcastChannel` delivery remains same-origin browser messaging and helps
 * when providers or browsers make `window.opener` unavailable.
 */
export function handleMcpOAuthCallback(
  options: {
    closeWindow?: boolean;
    targetOrigin?: string;
  } = {},
): McpOAuthCallbackResult {
  const result = readMcpOAuthCallbackResult(options);
  const targetOrigin = options.targetOrigin ?? window.location.origin;

  window.opener?.postMessage(result.message, targetOrigin);

  const channel = new BroadcastChannel(MCP_OAUTH_CALLBACK_CHANNEL);
  channel.postMessage(result.message);
  channel.close();

  if (result.closeWindow) {
    window.close();
  }

  return result;
}

function readMcpOAuthCallbackResult(
  options: {
    closeWindow?: boolean;
  } = {},
): McpOAuthCallbackResult {
  const params = new URLSearchParams(window.location.search);
  const message: McpOAuthCallbackMessage = {
    ...(params.get("code") ? { code: params.get("code")! } : {}),
    ...(params.get("error") ? { error: params.get("error")! } : {}),
    ...(params.get("error_description")
      ? { errorDescription: params.get("error_description")! }
      : {}),
    ...(params.get("state") ? { state: params.get("state")! } : {}),
    type: "use-mcp-react:oauth-callback",
  };
  const shouldClose = options.closeWindow !== false && !message.error;

  return {
    closeWindow: shouldClose,
    message,
  };
}

/**
 * Minimal React callback page for the default `/oauth/callback` route.
 *
 * Mount this component at the redirect URL configured with the authorization
 * server. It posts the callback result to the opener and through
 * `BroadcastChannel`, then renders a fallback page for browsers that cannot
 * close the popup automatically.
 */
export function McpOAuthCallback(): ReactElement {
  const [result, setResult] = useState(() => readMcpOAuthCallbackResult());
  const handledCallbackRef = useRef(false);

  useEffect(() => {
    if (handledCallbackRef.current) {
      return;
    }

    handledCallbackRef.current = true;
    setResult(handleMcpOAuthCallback());
  }, []);

  return createElement(
    "main",
    {
      style: {
        color: "#18231f",
        fontFamily:
          'Inter, ui-sans-serif, system-ui, -apple-system, BlinkMacSystemFont, "Segoe UI", sans-serif',
        padding: "32px",
      },
    },
    createElement(
      "h1",
      null,
      result.message.error ? "Authorization failed" : "Authorization complete",
    ),
    createElement(
      "p",
      null,
      result.message.error
        ? (result.message.errorDescription ?? result.message.error)
        : result.closeWindow
          ? "This window can be closed."
          : "Return to the application to continue.",
    ),
  );
}

/**
 * Connects a React component to one Streamable HTTP MCP server.
 *
 * The hook probes the server when `url` is present and `enabled !== false`,
 * then classifies the next browser-visible action: no auth, OAuth consent,
 * bearer token entry, ready, or failure. It intentionally uses the real MCP
 * SDK client and Streamable HTTP transport so initialization, catalog calls,
 * OAuth discovery, PKCE, Dynamic Client Registration, token refresh, and
 * Resource Indicators stay on the protocol path.
 *
 * Use one hook instance per MCP server. That keeps auth state, token audience,
 * server capabilities, and catalogs scoped to the resource server that issued
 * them.
 *
 * @see https://modelcontextprotocol.io/specification/2025-11-25/basic/authorization
 * @see https://modelcontextprotocol.io/specification/2025-11-25/basic/transports
 * @see https://datatracker.ietf.org/doc/html/rfc9728
 * @see https://datatracker.ietf.org/doc/html/rfc8707
 */
export function useMcp(options: UseMcpOptions): UseMcpResult {
  const [state, setState] = useState<UseMcpState>(IDLE_STATE);
  const connectionRef = useRef<Connection | null>(null);
  const connectionGenerationRef = useRef(0);
  const optionsRef = useRef(options);
  const oauthProviderKeyRef = useRef<string | null>(null);
  const oauthProviderRef = useRef<PendingOAuthProvider | null>(null);
  const pendingOAuthConnectionRef = useRef<PendingOAuthConnection | null>(null);
  const pendingOAuthFinishPromiseRef = useRef<Promise<McpActionResult> | null>(null);
  const popupRef = useRef<Window | null>(null);
  const popupOwnedByHookRef = useRef(false);
  const popupWindowNameRef = useRef<string | null>(null);
  const stateRef = useRef(state);

  optionsRef.current = options;
  stateRef.current = state;

  const closeCurrentConnections = useCallback(async () => {
    const connection = connectionRef.current;
    const pendingOAuthConnection = pendingOAuthConnectionRef.current?.connection;
    connectionRef.current = null;
    pendingOAuthConnectionRef.current = null;
    pendingOAuthFinishPromiseRef.current = null;

    if (connection) {
      await closeConnection(connection);
    }
    if (pendingOAuthConnection && pendingOAuthConnection !== connection) {
      await closeConnection(pendingOAuthConnection);
    }
  }, []);

  const closeHookOwnedPopup = useCallback(() => {
    const popup = popupRef.current;
    if (
      popup &&
      popupOwnedByHookRef.current &&
      !popup.closed &&
      typeof popup.close === "function"
    ) {
      popup.close();
    }
    popupRef.current = null;
    popupOwnedByHookRef.current = false;
    popupWindowNameRef.current = null;
  }, []);

  const oauthPopupWindowName = useCallback(() => {
    popupWindowNameRef.current ??= createOAuthPopupWindowName();

    return popupWindowNameRef.current;
  }, []);

  const connectWithOptions = useCallback(
    async (
      connectionOptions: UseMcpActionOptions,
      existingOAuthProvider?: PendingOAuthProvider,
      lifecycle: "connect" | "reconnect" = "connect",
    ): Promise<McpActionResult> => {
      const url = normalizeMcpUrl(connectionOptions.url);
      const generation = connectionGenerationRef.current + 1;
      connectionGenerationRef.current = generation;

      if (connectionOptions.enabled === false || !url) {
        await closeCurrentConnections();
        closeHookOwnedPopup();
        if (connectionGenerationRef.current !== generation) {
          return { ok: true };
        }
        setState(IDLE_STATE);
        return { ok: true };
      }

      closeHookOwnedPopup();

      let lastAuthenticateHeader: string | null = null;
      const client = new Client({ name: "use-mcp-react", version: MCP_CLIENT_VERSION });
      const oauthProviderKey = createOAuthProviderKey(url, connectionOptions);
      const oauthProvider = connectionOptions.bearerToken
        ? undefined
        : (existingOAuthProvider ??
          (oauthProviderRef.current && oauthProviderKeyRef.current === oauthProviderKey
            ? oauthProviderRef.current
            : new PendingOAuthProvider(
                url,
                connectionOptions.oauth,
                resolveMcpStorage(connectionOptions.storage),
              )));
      if (!existingOAuthProvider) {
        oauthProvider?.clearAuthenticationChallenge();
      }
      const authorizationTargetResult = prepareAuthorizationTarget(
        oauthProvider,
        connectionOptions.authorizationTarget,
        oauthPopupWindowName,
      );
      if (!authorizationTargetResult.ok) {
        return authorizationTargetResult;
      }
      if (authorizationTargetResult.target) {
        popupRef.current = authorizationTargetResult.target;
        popupOwnedByHookRef.current = authorizationTargetResult.ownsTarget === true;
      }
      oauthProviderRef.current = oauthProvider ?? null;
      oauthProviderKeyRef.current = oauthProvider ? oauthProviderKey : null;
      const authProvider = connectionOptions.bearerToken
        ? createStaticBearerAuthProvider(connectionOptions.bearerToken)
        : oauthProvider;
      const transport = new StreamableHTTPClientTransport(url, {
        ...(authProvider ? { authProvider } : {}),
        fetch: createAuthenticationChallengeCapturingFetch({
          oauthProvider,
          onAuthenticateHeader: (header) => {
            lastAuthenticateHeader = header;
            oauthProvider?.captureAuthenticationChallenge(header);
          },
          serverUrl: url,
          transportProxy: connectionOptions.transportProxy,
        }),
      });
      const connection = { client, transport };
      await closeCurrentConnections();
      if (connectionGenerationRef.current !== generation) {
        await closeConnection(connection);
        return { ok: true };
      }
      connectionRef.current = connection;
      const preserveCatalog = lifecycle === "reconnect";

      setState((current) => ({
        ...(preserveCatalog ? current : IDLE_STATE),
        authRequirement: null,
        authDiagnostics: null,
        authorizationUrl: null,
        client: null,
        error: null,
        status: preserveCatalog ? "reconnecting" : "connecting",
        transport: null,
      }));
      if (preserveCatalog) {
        await yieldToBrowser(RECONNECT_STATUS_PAINT_DELAY_MS);
        if (connectionGenerationRef.current !== generation) {
          if (connectionRef.current === connection) {
            connectionRef.current = null;
          }
          await closeConnection(connection);
          return { ok: true };
        }
      }

      try {
        await client.connect(transport);
        if (connectionGenerationRef.current !== generation) {
          if (connectionRef.current === connection) {
            connectionRef.current = null;
          }
          await closeConnection(connection);
          return { ok: true };
        }
        const serverCapabilities = client.getServerCapabilities() ?? null;
        const serverVersion = client.getServerVersion() ?? null;
        const initialize = createInitializeResult(client, transport, {
          serverCapabilities,
          serverVersion,
        });
        setState((current) => ({
          ...current,
          authRequirement: null,
          authDiagnostics: null,
          authorizationUrl: null,
          catalogErrors: {},
          catalogStatus: "loading",
          client: null,
          error: null,
          serverCapabilities,
          serverProfile: null,
          serverVersion,
          status: "loading",
          transport: null,
        }));
        const catalog = await loadCatalog(client, serverCapabilities);

        if (connectionGenerationRef.current !== generation) {
          await closeConnection(connection);
          return { ok: true };
        }

        closeHookOwnedPopup();
        setState({
          authRequirement: null,
          authDiagnostics: null,
          authorizationUrl: null,
          catalogErrors: catalog.errors,
          catalogStatus: catalog.status,
          client,
          error: null,
          prompts: catalog.prompts,
          resourceTemplates: catalog.resourceTemplates,
          resources: catalog.resources,
          serverCapabilities,
          serverProfile: createServerProfile({
            auth: createAuthProfile(connectionOptions, oauthProvider),
            catalog,
            initialize,
            transport,
            url,
          }),
          serverVersion,
          status: "ready",
          tools: catalog.tools,
          transport,
        });
        pendingOAuthConnectionRef.current = null;
        return { ok: true };
      } catch (cause) {
        if (connectionGenerationRef.current !== generation) {
          await closeConnection(connection);
          return { ok: true };
        }

        const bearerRequirement = parseBearerAuthRequirement(lastAuthenticateHeader);
        if (bearerRequirement) {
          await closeConnection(connection);
          closeHookOwnedPopup();
          if (connectionGenerationRef.current !== generation) {
            return { ok: true };
          }
          setState((current) => ({
            ...(preserveCatalog ? current : IDLE_STATE),
            authRequirement: bearerRequirement,
            authDiagnostics: null,
            authorizationUrl: null,
            client: null,
            error: null,
            status: "pending_auth",
            transport: null,
          }));
          return { ok: true };
        }

        const oauthRequirement = oauthProvider?.authRequirement();
        const oauthTokens = oauthProvider ? await oauthProvider.tokens() : undefined;
        if (oauthRequirement && oauthProvider && !oauthTokens) {
          pendingOAuthConnectionRef.current = {
            connection,
            options: connectionOptions,
            provider: oauthProvider,
          };
          setState((current) => ({
            ...(preserveCatalog ? current : IDLE_STATE),
            authRequirement: oauthRequirement,
            authDiagnostics: oauthProvider.authDiagnostics(),
            authorizationUrl: oauthRequirement.authorizationUrl,
            client: null,
            error: null,
            status: "pending_auth",
            transport: null,
          }));
          return { ok: true };
        }

        const manualOAuthInference = await inferManualOAuthClientRequirement(
          url,
          lastAuthenticateHeader,
        );
        if (connectionGenerationRef.current !== generation) {
          await closeConnection(connection);
          return { ok: true };
        }
        if (manualOAuthInference) {
          await closeConnection(connection);
          closeHookOwnedPopup();
          if (connectionGenerationRef.current !== generation) {
            return { ok: true };
          }
          setState((current) => ({
            ...(preserveCatalog ? current : IDLE_STATE),
            authRequirement: manualOAuthInference.requirement,
            authDiagnostics: {
              authorizationServerMetadataUrl: manualOAuthInference.authorizationServerMetadataUrl,
              issuer: manualOAuthInference.requirement.issuer,
              resourceMetadataUrl: manualOAuthInference.resourceMetadataUrl,
            },
            authorizationUrl: null,
            client: null,
            error: null,
            status: "pending_auth",
            transport: null,
          }));
          return { ok: true };
        }

        await closeConnection(connection);
        closeHookOwnedPopup();
        if (connectionGenerationRef.current !== generation) {
          return { ok: true };
        }
        setState((current) => ({
          ...(preserveCatalog ? current : IDLE_STATE),
          authRequirement: null,
          authDiagnostics: null,
          authorizationUrl: null,
          client: null,
          error: cause instanceof Error ? cause : new Error(String(cause)),
          status: "failed",
          transport: null,
        }));
        return {
          ok: false,
          reason: "failed",
          error: cause instanceof Error ? cause : new Error(String(cause)),
        };
      }
    },
    [closeCurrentConnections, closeHookOwnedPopup, oauthPopupWindowName],
  );

  const connect = useCallback(
    async (overrideOptions?: UseMcpActionOptions) => {
      return connectWithOptions(mergeUseMcpActionOptions(optionsRef.current, overrideOptions));
    },
    [connectWithOptions],
  );
  const disconnect = useCallback(async (): Promise<McpActionResult> => {
    const generation = connectionGenerationRef.current + 1;
    connectionGenerationRef.current = generation;
    const connection = connectionRef.current;
    connectionRef.current = null;

    if (connection) {
      await closeConnection(connection);
    }
    if (connectionGenerationRef.current !== generation) {
      return { ok: true };
    }
    pendingOAuthConnectionRef.current = null;
    pendingOAuthFinishPromiseRef.current = null;
    closeHookOwnedPopup();

    setState((current) => ({
      ...current,
      authRequirement: null,
      authDiagnostics: null,
      authorizationUrl: null,
      client: null,
      error: null,
      status: "idle",
      transport: null,
    }));
    return { ok: true };
  }, [closeHookOwnedPopup]);
  const reconnect = useCallback(
    async (overrideOptions?: UseMcpActionOptions): Promise<McpActionResult> => {
      const generation = connectionGenerationRef.current + 1;
      connectionGenerationRef.current = generation;
      const connection = connectionRef.current;
      connectionRef.current = null;
      pendingOAuthConnectionRef.current = null;
      pendingOAuthFinishPromiseRef.current = null;
      closeHookOwnedPopup();

      if (connection) {
        await closeConnection(connection);
      }
      if (connectionGenerationRef.current !== generation) {
        return { ok: true };
      }

      return connectWithOptions(
        mergeUseMcpActionOptions(optionsRef.current, overrideOptions),
        undefined,
        "reconnect",
      );
    },
    [closeHookOwnedPopup, connectWithOptions],
  );
  const reauthorize = useCallback(
    async (overrideOptions?: UseMcpActionOptions): Promise<McpActionResult> => {
      const generation = connectionGenerationRef.current + 1;
      connectionGenerationRef.current = generation;
      const connection = connectionRef.current;
      const pendingOAuthConnection = pendingOAuthConnectionRef.current?.connection;
      connectionRef.current = null;
      pendingOAuthConnectionRef.current = null;
      pendingOAuthFinishPromiseRef.current = null;
      closeHookOwnedPopup();

      const provider = oauthProviderRef.current;
      if (provider) {
        await provider.invalidateCredentials("tokens");
        await provider.invalidateCredentials("verifier");
        if (overrideOptions?.clearClient) {
          await provider.invalidateCredentials("client");
        }
        if (overrideOptions?.clearDiscovery) {
          await provider.invalidateCredentials("discovery");
        }
      }

      if (connection) {
        await closeConnection(connection);
      }
      if (pendingOAuthConnection && pendingOAuthConnection !== connection) {
        await closeConnection(pendingOAuthConnection);
      }
      if (connectionGenerationRef.current !== generation) {
        return { ok: true };
      }

      return connectWithOptions(
        mergeUseMcpActionOptions(optionsRef.current, overrideOptions),
        undefined,
        "reconnect",
      );
    },
    [closeHookOwnedPopup, connectWithOptions],
  );
  const forget = useCallback(async (): Promise<McpActionResult> => {
    const generation = connectionGenerationRef.current + 1;
    connectionGenerationRef.current = generation;
    const connection = connectionRef.current;
    const pendingOAuthConnection = pendingOAuthConnectionRef.current?.connection;
    const provider = oauthProviderRef.current;
    connectionRef.current = null;
    pendingOAuthConnectionRef.current = null;
    pendingOAuthFinishPromiseRef.current = null;
    oauthProviderKeyRef.current = null;
    oauthProviderRef.current = null;
    closeHookOwnedPopup();

    if (connection) {
      await closeConnection(connection);
    }
    if (pendingOAuthConnection && pendingOAuthConnection !== connection) {
      await closeConnection(pendingOAuthConnection);
    }
    await provider?.invalidateCredentials("all");
    if (connectionGenerationRef.current !== generation) {
      return { ok: true };
    }

    setState(IDLE_STATE);
    return { ok: true };
  }, [closeHookOwnedPopup]);
  const authorize = useCallback(
    async (options?: { target?: AuthorizationTarget | "popup" }): Promise<McpActionResult> => {
      const authorizationUrl = stateRef.current.authorizationUrl;
      if (!authorizationUrl) {
        return { ok: false, reason: "no_pending_authorization" };
      }

      const requestedTarget = options?.target;
      if (requestedTarget && requestedTarget !== "popup") {
        requestedTarget.location.href = authorizationUrl.toString();
        requestedTarget.focus();
        return { ok: true };
      }

      const popup = popupRef.current;
      if (popup && !popup.closed) {
        popup.focus();
        return { ok: true };
      }

      const openedPopup = window.open(
        authorizationUrl.toString(),
        oauthPopupWindowName(),
        "width=600,height=700,resizable=yes,scrollbars=yes,status=yes",
      );
      if (!openedPopup) {
        return { ok: false, reason: "popup_blocked" };
      }
      popupRef.current = openedPopup;
      popupOwnedByHookRef.current = true;
      openedPopup.focus();
      return { ok: true };
    },
    [oauthPopupWindowName],
  );
  const finishAuthorization = useCallback(
    async (authorizationCode: string, state: string): Promise<McpActionResult> => {
      const existingFinish = pendingOAuthFinishPromiseRef.current;
      if (existingFinish) {
        return existingFinish;
      }

      const pendingOAuthConnection = pendingOAuthConnectionRef.current;
      const generation = connectionGenerationRef.current;

      if (!pendingOAuthConnection) {
        setState({
          ...IDLE_STATE,
          error: new Error("No pending OAuth authorization."),
          status: "failed",
        });
        return { ok: false, reason: "no_pending_authorization" };
      }

      const expectedState = pendingOAuthConnection.provider.pendingState();
      if (!expectedState) {
        return { ok: false, reason: "missing_oauth_state" };
      }
      if (state !== expectedState) {
        return { ok: false, reason: "oauth_state_mismatch" };
      }

      let finishPromise!: Promise<McpActionResult>;
      finishPromise = (async (): Promise<McpActionResult> => {
        setState((current) => ({
          ...current,
          error: null,
          status: "authenticating",
        }));
        await yieldToBrowser(AUTHENTICATING_STATUS_PAINT_DELAY_MS);
        if (
          connectionGenerationRef.current !== generation ||
          pendingOAuthConnectionRef.current !== pendingOAuthConnection
        ) {
          return { ok: true };
        }

        try {
          await pendingOAuthConnection.connection.transport.finishAuth(authorizationCode);
          if (
            connectionGenerationRef.current !== generation ||
            pendingOAuthConnectionRef.current !== pendingOAuthConnection
          ) {
            return { ok: true };
          }
          if (connectionRef.current === pendingOAuthConnection.connection) {
            connectionRef.current = null;
          }
          await closeConnection(pendingOAuthConnection.connection);
          pendingOAuthConnectionRef.current = null;
          return connectWithOptions(
            { ...pendingOAuthConnection.options, authorizationTarget: undefined },
            pendingOAuthConnection.provider,
          );
        } catch (cause) {
          if (
            connectionGenerationRef.current !== generation ||
            pendingOAuthConnectionRef.current !== pendingOAuthConnection
          ) {
            return { ok: true };
          }
          pendingOAuthConnectionRef.current = null;
          if (connectionRef.current === pendingOAuthConnection.connection) {
            connectionRef.current = null;
          }
          await closeConnection(pendingOAuthConnection.connection);
          setState({
            ...IDLE_STATE,
            error: cause instanceof Error ? cause : new Error(String(cause)),
            status: "failed",
          });
          return {
            ok: false,
            reason: "failed",
            error: cause instanceof Error ? cause : new Error(String(cause)),
          };
        } finally {
          if (pendingOAuthFinishPromiseRef.current === finishPromise) {
            pendingOAuthFinishPromiseRef.current = null;
          }
        }
      })();
      pendingOAuthFinishPromiseRef.current = finishPromise;
      return finishPromise;
    },
    [connectWithOptions],
  );

  useEffect(() => {
    const finishFromMessage = (message: McpOAuthCallbackMessage) => {
      const pendingOAuthConnection = pendingOAuthConnectionRef.current;
      const expectedState = pendingOAuthConnection?.provider.pendingState();
      if (!pendingOAuthConnection || !expectedState || message.state !== expectedState) {
        return;
      }

      if (message.error) {
        pendingOAuthConnectionRef.current = null;
        pendingOAuthFinishPromiseRef.current = null;
        if (connectionRef.current === pendingOAuthConnection.connection) {
          connectionRef.current = null;
        }
        closeHookOwnedPopup();
        void (async () => {
          await pendingOAuthConnection.provider.invalidateCredentials("verifier");
          await closeConnection(pendingOAuthConnection.connection);
          setState({
            ...IDLE_STATE,
            error: new Error(message.errorDescription ?? message.error),
            status: "failed",
          });
        })();
        return;
      }

      if (!message.code) {
        return;
      }

      void finishAuthorization(message.code, message.state);
    };

    const handleWindowMessage = (event: MessageEvent<unknown>) => {
      if (event.origin !== window.location.origin || !isMcpOAuthCallbackMessage(event.data)) {
        return;
      }

      finishFromMessage(event.data);
    };
    const channel = new BroadcastChannel(MCP_OAUTH_CALLBACK_CHANNEL);
    channel.onmessage = (event) => {
      if (isMcpOAuthCallbackMessage(event.data)) {
        finishFromMessage(event.data);
      }
    };

    window.addEventListener("message", handleWindowMessage);

    return () => {
      channel.close();
      window.removeEventListener("message", handleWindowMessage);
    };
  }, [closeHookOwnedPopup, finishAuthorization]);

  const oauthClientMetadataKey = stableSerializeOAuthClientMetadata(options.oauth?.clientMetadata);
  const bearerTokenKey = options.bearerToken;
  const mcpUrlKey = stableSerializeUrlDependency(options.url);
  const redirectUrlKey = stableSerializeOAuthRedirectUrlDependency(options.oauth?.redirectUrl);
  const transportProxyKey = stableSerializeTransportProxyDependency(options.transportProxy);
  const storageKey = getMcpStorageDependencyKey(options.storage);

  useEffect(() => {
    const url = normalizeMcpUrl(options.url);
    if (options.enabled === false || !url) {
      connectionGenerationRef.current += 1;
      setState(IDLE_STATE);
      return;
    }

    void connectWithOptions(options);

    return () => {
      connectionGenerationRef.current += 1;
      const connection = connectionRef.current;
      const pendingOAuthConnection = pendingOAuthConnectionRef.current?.connection;
      connectionRef.current = null;
      pendingOAuthFinishPromiseRef.current = null;
      closeHookOwnedPopup();

      if (connection) {
        void closeConnection(connection);
      }
      if (pendingOAuthConnection && pendingOAuthConnection !== connection) {
        void closeConnection(pendingOAuthConnection);
      }
      pendingOAuthConnectionRef.current = null;
    };
  }, [
    connectWithOptions,
    bearerTokenKey,
    options.enabled,
    options.oauth?.clientId,
    oauthClientMetadataKey,
    options.oauth?.clientMetadataUrl,
    redirectUrlKey,
    storageKey,
    mcpUrlKey,
    transportProxyKey,
    closeHookOwnedPopup,
  ]);

  return useMemo(
    () => ({
      authRequirement: state.authRequirement,
      authDiagnostics: state.authDiagnostics,
      authorizationUrl: state.authorizationUrl,
      authorize,
      catalogErrors: state.catalogErrors,
      catalogStatus: state.catalogStatus,
      client: state.client,
      connect,
      disconnect,
      error: state.error,
      finishAuthorization,
      forget,
      prompts: state.prompts,
      reauthorize,
      reconnect,
      resourceTemplates: state.resourceTemplates,
      resources: state.resources,
      serverCapabilities: state.serverCapabilities,
      serverProfile: state.serverProfile,
      serverVersion: state.serverVersion,
      status: state.status,
      tools: state.tools,
      transport: state.transport,
    }),
    [authorize, connect, disconnect, finishAuthorization, forget, reauthorize, reconnect, state],
  );
}

function stableSerializeOAuthClientMetadata(
  clientMetadata: OAuthClientMetadata | undefined,
): string | undefined {
  return clientMetadata ? stableSerializeJsonValue(clientMetadata) : undefined;
}

function mergeUseMcpActionOptions(
  options: UseMcpOptions,
  overrideOptions: UseMcpActionOptions | undefined,
): UseMcpActionOptions {
  if (!overrideOptions) {
    return options;
  }

  const oauth =
    overrideOptions.oauth || options.oauth
      ? {
          ...options.oauth,
          ...overrideOptions.oauth,
        }
      : undefined;

  return {
    ...options,
    ...overrideOptions,
    ...(oauth ? { oauth } : {}),
  };
}

function stableSerializeUrlDependency(url: URL | string | null | undefined): string | null {
  return normalizeMcpUrl(url)?.toString() ?? null;
}

function stableSerializeOAuthRedirectUrlDependency(url: URL | string | undefined): string | null {
  return url ? resolveOAuthRedirectUrl(url) : null;
}

function stableSerializeTransportProxyDependency(
  transportProxy: UseMcpTransportProxy | undefined,
): string | null {
  return transportProxy ? transportProxy.toString() : null;
}

function authorizationServerMetadataUrlFor(authorizationServerUrl: URL | string): string {
  const url = new URL(authorizationServerUrl);
  let pathname = url.pathname;
  if (pathname.endsWith("/")) {
    pathname = pathname.slice(0, -1);
  }

  return new URL(
    pathname
      ? `/.well-known/oauth-authorization-server${pathname}`
      : "/.well-known/oauth-authorization-server",
    url.origin,
  ).toString();
}

function stableSerializeJsonValue(value: unknown): string {
  if (value === null || typeof value !== "object") {
    return JSON.stringify(value) ?? "null";
  }

  if (Array.isArray(value)) {
    return `[${value.map((item) => stableSerializeJsonValue(item)).join(",")}]`;
  }

  const record = value as Record<string, unknown>;
  return `{${Object.keys(record)
    .filter((key) => record[key] !== undefined)
    .sort()
    .map((key) => `${JSON.stringify(key)}:${stableSerializeJsonValue(record[key])}`)
    .join(",")}}`;
}

function normalizeMcpUrl(url: URL | string | null | undefined): URL | null {
  if (url == null) {
    return null;
  }

  const value = typeof url === "string" ? url.trim() : url.toString();
  if (!value) {
    return null;
  }

  try {
    return new URL(value);
  } catch {
    return null;
  }
}

function normalizeTransportProxyUrl(transportProxy: UseMcpTransportProxy | undefined): URL | null {
  if (!transportProxy) {
    return null;
  }

  const value =
    typeof transportProxy === "string" ? transportProxy.trim() : transportProxy.toString();
  if (!value) {
    throw new Error("transportProxy must be a non-empty URL.");
  }

  const baseUrl = typeof window === "undefined" ? "http://localhost" : window.location.origin;

  let url: URL;
  try {
    url = new URL(value, baseUrl);
  } catch {
    throw new Error(`Invalid transportProxy URL: ${value}`);
  }

  if (url.origin !== new URL(baseUrl).origin) {
    throw new Error("transportProxy must be same-origin.");
  }

  return url;
}

async function loadCatalog(
  client: Client,
  serverCapabilities: ServerCapabilities | null,
): Promise<{
  errors: CatalogErrors;
  prompts: Prompt[];
  resourceTemplates: ResourceTemplate[];
  resources: Resource[];
  status: CatalogStatus;
  tools: Tool[];
  snapshot: McpCatalogSnapshot;
}> {
  const toolsPromise: Promise<LoadedCatalogSection<Tool>> = serverCapabilities?.tools
    ? loadCatalogSection((cursor) => client.listTools(cursor ? { cursor } : undefined), "tools")
    : Promise.resolve(createLoadedCatalogSection(EMPTY_TOOLS));
  const resourcesPromise: Promise<LoadedCatalogSection<Resource>> = serverCapabilities?.resources
    ? loadCatalogSection(
        (cursor) => client.listResources(cursor ? { cursor } : undefined),
        "resources",
      )
    : Promise.resolve(createLoadedCatalogSection(EMPTY_RESOURCES));
  const resourceTemplatesPromise: Promise<LoadedCatalogSection<ResourceTemplate>> =
    serverCapabilities?.resources
      ? loadCatalogSection(
          (cursor) => client.listResourceTemplates(cursor ? { cursor } : undefined),
          "resourceTemplates",
        )
      : Promise.resolve(createLoadedCatalogSection(EMPTY_RESOURCE_TEMPLATES));
  const promptsPromise: Promise<LoadedCatalogSection<Prompt>> = serverCapabilities?.prompts
    ? loadCatalogSection((cursor) => client.listPrompts(cursor ? { cursor } : undefined), "prompts")
    : Promise.resolve(createLoadedCatalogSection(EMPTY_PROMPTS));

  const [tools, resources, resourceTemplates, prompts] = await Promise.allSettled([
    toolsPromise,
    resourcesPromise,
    resourceTemplatesPromise,
    promptsPromise,
  ]);
  const errors: CatalogErrors = {};

  if (tools.status === "rejected") {
    errors.tools = tools.reason;
  }
  if (resources.status === "rejected") {
    errors.resources = resources.reason;
  }
  if (resourceTemplates.status === "rejected") {
    errors.resourceTemplates = resourceTemplates.reason;
  }
  if (prompts.status === "rejected") {
    errors.prompts = prompts.reason;
  }

  const errorCount = Object.keys(errors).length;

  const toolsValue = tools.status === "fulfilled" ? tools.value.items : EMPTY_TOOLS;
  const resourcesValue = resources.status === "fulfilled" ? resources.value.items : EMPTY_RESOURCES;
  const resourceTemplatesValue =
    resourceTemplates.status === "fulfilled"
      ? resourceTemplates.value.items
      : EMPTY_RESOURCE_TEMPLATES;
  const promptsValue = prompts.status === "fulfilled" ? prompts.value.items : EMPTY_PROMPTS;

  return {
    errors,
    prompts: promptsValue,
    resourceTemplates: resourceTemplatesValue,
    resources: resourcesValue,
    status: errorCount === 0 ? "ready" : errorCount === 4 ? "error" : "partial",
    tools: toolsValue,
    snapshot: {
      prompts: createCatalogSection(promptsValue, prompts),
      resourceTemplates: createCatalogSection(resourceTemplatesValue, resourceTemplates),
      resources: createCatalogSection(resourcesValue, resources),
      tools: createCatalogSection(toolsValue, tools),
    },
  };
}

type LoadedCatalogSection<Item> = {
  complete: boolean;
  items: Item[];
  nextCursor?: string;
};

async function loadCatalogSection<
  Item,
  Key extends string,
  Result extends { nextCursor?: string } & Record<Key, Item[]>,
>(
  loadPage: (cursor?: string) => Promise<Result>,
  itemKey: Key,
): Promise<LoadedCatalogSection<Item>> {
  const items: Item[] = [];
  let cursor: string | undefined;

  for (let page = 0; page < MAX_CATALOG_PAGES; page += 1) {
    const result = await loadPage(cursor);
    const pageItems = result[itemKey];
    if (Array.isArray(pageItems)) {
      items.push(...(pageItems as Item[]));
    }

    cursor = result.nextCursor;
    if (!cursor) {
      return {
        complete: true,
        items,
      };
    }
  }

  return {
    complete: false,
    items,
    nextCursor: cursor,
  };
}

function createLoadedCatalogSection<Item>(items: Item[]): LoadedCatalogSection<Item> {
  return {
    complete: true,
    items,
  };
}

