/**
 * Public connection state for one MCP server.
 *
 * The states are intentionally about what a browser application can do next, not
 * SDK implementation phases. That keeps UI code from depending on SDK errors or
 * transport internals while still exposing enough detail for runtime setup flows.
 *
 * @see https://modelcontextprotocol.io/specification/2025-11-25/basic/authorization
 */
export type UseMcpStatus =
  | "idle"
  | "connecting"
  | "pending_auth"
  | "authenticating"
  | "loading"
  | "ready"
  | "reconnecting"
  | "failed";

/**
 * OAuth authorization is required before the MCP connection can be established.
 *
 * The hook prepares this from the SDK-generated authorization request so apps can
 * render a consent button without reconstructing OAuth parameters themselves. MCP
 * authorization uses Protected Resource Metadata for discovery, Authorization
 * Server Metadata for endpoints/capabilities, PKCE for browser public clients,
 * and Resource Indicators so issued tokens are bound to the target MCP server.
 *
 * @see https://modelcontextprotocol.io/specification/2025-11-25/basic/authorization
 * @see https://datatracker.ietf.org/doc/html/rfc9728
 * @see https://datatracker.ietf.org/doc/html/rfc8414
 * @see https://datatracker.ietf.org/doc/html/rfc7636
 * @see https://datatracker.ietf.org/doc/html/rfc8707
 */
export type McpOAuthRequirement = {
  /**
   * Fully prepared authorization request generated by the MCP SDK.
   *
   * Apps should pass this through `authorize()` from a user gesture instead of
   * opening it automatically; browsers may block popups that are not directly
   * user initiated.
   */
  authorizationUrl: URL;
  /**
   * Authorization server issuer, when discovery returned one.
   */
  issuer?: string;
  /**
   * Scopes requested for the current MCP server challenge.
   *
   * MCP treats challenge scopes as authoritative for the failed request so the
   * client does not over-request broader access than the server asked for.
   */
  scopes?: string[];
  /**
   * Whether URL-based Client ID Metadata Documents are advertised by the
   * authorization server.
   *
   * This is surfaced because browser clients often have no prior relationship
   * with arbitrary MCP servers. CIMD avoids dynamic registration when both sides
   * support it.
   *
   * @see https://datatracker.ietf.org/doc/html/draft-ietf-oauth-client-id-metadata-document-00
   */
  supportsClientMetadataDocument: boolean;
  /**
   * Whether Dynamic Client Registration is available as a fallback.
   *
   * DCR lets a browser client obtain a public client id at runtime when there is
   * no pre-registered client and CIMD is not available.
   *
   * @see https://datatracker.ietf.org/doc/html/rfc7591
   */
  supportsDynamicClientRegistration: boolean;
  type: "oauth";
};

/**
 * Bearer/API-key style auth is required, but the server did not advertise MCP
 * OAuth metadata.
 *
 * In this case the app should collect or provide a bearer token itself. The hook
 * does not persist these tokens because API-key storage policy is application
 * specific.
 *
 * @see https://datatracker.ietf.org/doc/html/rfc6750
 * @see https://datatracker.ietf.org/doc/html/rfc9728
 */
export type McpBearerAuthRequirement = {
  reason: "oauth_metadata_absent";
  realm?: string;
  scopes?: string[];
  type: "bearer";
};

/**
 * OAuth metadata was discovered, but the authorization server cannot produce a
 * browser public client automatically.
 *
 * Apps should collect a pre-registered public `clientId` and reconnect with
 * `oauth.clientId`. The hook still handles PKCE, state validation, token
 * exchange, and MCP connection lifecycle after the client id is supplied.
 */
export type McpManualOAuthClientRequirement = {
  authorizationEndpoint?: string;
  issuer?: string;
  reason: "client_registration_unavailable";
  suggestedFields: ["clientId"];
  supportsClientMetadataDocument: false;
  supportsDynamicClientRegistration: false;
  tokenEndpoint?: string;
  type: "manual_oauth_client";
};

/**
 * Normalized auth diagnosis for one MCP server.
 *
 * This is the public alternative to making app code inspect SDK exception text
 * or raw `WWW-Authenticate` headers.
 */
export type McpAuthRequirement =
  | McpBearerAuthRequirement
  | McpManualOAuthClientRequirement
  | McpOAuthRequirement;

/**
 * OAuth client configuration for MCP servers that cannot rely on default DCR.
 *
 * Leave this empty for the normal remote-MCP path: the SDK will use MCP
 * authorization discovery and Dynamic Client Registration when the server
 * advertises it. Provide `clientId` for authorization servers, such as many
 * Google/Gmail deployments, that require a client to be registered out of band.
 * Provide `clientMetadataUrl` for servers that support Client ID Metadata
 * Documents and can use an HTTPS metadata document URL as the public client id.
 *
 * @see https://modelcontextprotocol.io/specification/2025-11-25/basic/authorization
 * @see https://datatracker.ietf.org/doc/html/draft-ietf-oauth-client-id-metadata-document-00
 * @see https://datatracker.ietf.org/doc/html/rfc7591
 */
export type UseMcpOAuthOptions = {
  /**
   * Pre-registered public OAuth client id.
   *
   * Use this when the authorization server does not expose Dynamic Client
   * Registration and cannot accept a Client ID Metadata Document URL. The hook
   * still uses PKCE and the SDK token exchange path; only client registration is
   * supplied by the host app.
   */
  clientId?: string;
  /**
   * OAuth client metadata sent during DCR or published by a CIMD document.
   *
   * The hook provides browser-safe defaults for a public PKCE client. Override
   * this when an MCP server requires a narrower scope, specific display name, or
   * metadata that matches a published Client ID Metadata Document.
   */
  clientMetadata?: OAuthClientMetadata;
  /**
   * HTTPS URL for a Client ID Metadata Document.
   *
   * The MCP SDK will use this as the client id only when the authorization
   * server advertises URL-based client identifiers. The URL must be HTTPS and
   * non-root because the authorization server fetches it independently.
   */
  clientMetadataUrl?: string;
  /**
   * OAuth callback URL registered with the authorization server.
   *
   * Defaults to `/oauth/callback` on the current browser origin. Manual client
   * registration must use the exact same redirect URI.
   */
  redirectUrl?: URL | string;
};

